    private void openRoleFrame(String username, String role) {
        setVisible(false);
        Runnable onLogout = () -> {
            passwordField.setText("");
            setLocationRelativeTo(null);
            setVisible(true);
            usernameField.requestFocusInWindow();
        };

        switch (role.toLowerCase()) {
            case "owner":
                new OwnerFrame(username, onLogout).setVisible(true);
                break;
            case "cashier":
                new CashierFrame(username, onLogout).setVisible(true);
                break;
            case "barista":
                new BaristaFrame(username, onLogout).setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Unrecognized role: " + role, "Authorization Error", JOptionPane.ERROR_MESSAGE);
                onLogout.run();
                break;
        }
    }

    private void loadLogo() {
        String sql = "SELECT asset_blob FROM system_assets WHERE asset_key = 'logo'";
        logoLabel.setText("Loading logo...");
        try (Connection conn = DBConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {
            if (rs.next()) {
                Blob logoBlob = rs.getBlob("asset_blob");
                ImageIcon icon = blobToImageIcon(logoBlob);
                if (icon != null) {
                    logoLabel.setIcon(icon);
                    logoLabel.setText("");
                } else {
                    logoLabel.setText("No Logo Available");
                }
            } else {
                logoLabel.setText("No Logo Available");
            }
        } catch (SQLException ex) {
            logoLabel.setText("No Logo Available");
        }
    }

    private ImageIcon blobToImageIcon(Blob blob) throws SQLException {
        if (blob == null) {
            return null;
        }

        try (InputStream inputStream = blob.getBinaryStream();
             ByteArrayOutputStream buffer = new ByteArrayOutputStream()) {
            byte[] data = new byte[4096];
            int bytesRead;
            while ((bytesRead = inputStream.read(data, 0, data.length)) != -1) {
                buffer.write(data, 0, bytesRead);
            }

            byte[] imageBytes = buffer.toByteArray();
            if (imageBytes.length == 0) {
                return null;
            }

            Image image = new ImageIcon(imageBytes).getImage();
            Image scaled = image.getScaledInstance(140, 140, Image.SCALE_SMOOTH);
            return new ImageIcon(scaled);
        } catch (IOException ex) {
            return null;
        }
    }

